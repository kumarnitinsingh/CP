bool canWePlace(vector<vector<int>>&v,int i,int j,int number,int n){
    for(int x=0;x<n;x++){
        if(v[x][j]==number || v[i][x]==number)return false;
    }

    //checking subgrid
    int r=sqrt(n);
    int x=(i/r)*r;
    int y=(j/r)*r;
    x=x;y=y;
    for(int i=x;i<x+r;i++){
        for(int j=y;j<y+r;j++){
            if(v[i][j]==number )return false;
        }
    }
return true;
}

bool sudokoSolve(vector<vector<int>>&grid,int i,int j,int n){
    if(i==n)return true;
    if(j==n){return sudokoSolve(grid,i+1,0,n);}
    if(grid[i][j]!=0){
        return sudokoSolve(grid,i,j+1,n);
    }

    for(int k=1;k<=n;k++){
        if(canWePlace(grid,i,j,k,n)){
            grid[i][j]=k;
            bool ans=sudokoSolve(grid,i,j+1,n);
            if(ans)return true;
        }
    }
     //backtrack
    grid[i][j]=0;
    return false;
}


